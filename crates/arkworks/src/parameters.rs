pub fn unsolved_hash<F: PrimeField>(unsolved: [[u8; 9]; 9]) -> F {
    let hash_result = sha2::Sha256::digest(
        &unsolved
            .into_iter()
            .map(|a| a.into_iter().map(|a| a))
            .flatten()
            .collect::<Vec<u8>>(),
    )
    .to_vec();

    F::from_le_bytes_mod_order(&hash_result[..31])
}
